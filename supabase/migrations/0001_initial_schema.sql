create index bookings_contact_email_idx on bookings (contact_email);
create index bookings_contact_phone_idx on bookings (contact_phone);
create index bookings_stripe_session_id_idx on bookings (stripe_checkout_session_id);
create index bookings_stripe_payment_intent_idx on bookings (stripe_payment_intent_id);
