-- =============================================================================
-- Row-level security
-- =============================================================================
-- RLS is enabled on every table with no policies defined. The Supabase
-- service-role key (used by the /api serverless functions) bypasses RLS, so
-- all current API access continues to work. The anon key has no access —
-- preventing the frontend from hitting Supabase directly. This matches the
-- existing architecture where all reads/writes go through /api.
-- =============================================================================

alter table classes                enable row level security;
alter table class_schedules        enable row level security;
alter table bookings               enable row level security;
alter table participants           enable row level security;
alter table persons                enable row level security;
alter table teaching_assignments   enable row level security;
alter table testimonials           enable row level security;
alter table satisfaction_surveys   enable row level security;
alter table form_submissions       enable row level security;
alter table categories             enable row level security;
alter table faq                    enable row level security;
alter table waiver_holding_table   enable row level security;
