  reminder_email_status text check (reminder_email_status in (
    'Sent', 'Delivered', 'Delayed', 'Bounced', 'Spam', 'Opened', 'Clicked'
  )),
  reminder_email_sent_at timestamptz,
  reminder_email_delivered_at timestamptz,
  reminder_email_clicked_at timestamptz,

