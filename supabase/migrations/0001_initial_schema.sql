-- =============================================================================
-- Streetwise Self Defense: Airtable → Supabase (Phase 1 — like-for-like schema)
-- =============================================================================
-- Tables mirror the 12 Airtable tables one-to-one. Airtable record IDs are
-- preserved in `airtable_record_id` so the Phase 2 data migration can resolve
-- linked-record references to Supabase UUID foreign keys.
--
-- Not included (intentional, for the reasons below):
--   • Lookup fields (e.g. Bookings."Class (from Class Schedule)",
--     Participants."Contact Email (from Booking)") — derive via JOIN.
--   • Formula fields (e.g. Class Schedules."Remaining Spots") — compute in
--     queries or add a view in Phase 3.
--   • Rollup fields (e.g. Class Schedules."Booked Spots") — same.
--   • Airtable "Created time" / "Last modified time" formula fields — replaced
--     by standard `created_at` / `updated_at` columns on every table.
-- =============================================================================

create extension if not exists pgcrypto;

create or replace function set_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at := now();
  return new;
end;
$$;


-- =============================================================================
-- 1. classes  (Airtable: Classes)
-- =============================================================================
create table classes (
  id uuid primary key default gen_random_uuid(),
  airtable_record_id text unique,

  class_id text,                              -- Airtable "ID" (single line text, primary field)
  class_name text not null,
  description text,
  type text check (type in (
    'Public: Adult & Teen',
    'Public: Mother & Daughter',
    'Private',
    'Corporate',
    'Community: Mother & Daughter'
  )),
  age_range text,
  duration integer,
  max_participants integer,
  location text,
  instructor text,
  price integer,
  booking_method text check (booking_method in (
    'External', 'Contact', 'SWSD website', 'Streetwise Self Defense'
  )),
  registration_instructions text,
  is_active boolean not null default false,
  partner_organization text check (partner_organization in (
    'City of Walnut Creek -- Arts & Recreation',
    'Diablo Valley College',
    'Streetwise Self Defense'
  )),
  city text check (city in (
    'Walnut Creek', 'Pleasant Hill', 'Richmond', 'San Francisco', 'San Jose'
  )),
  parking_instructions text,
  parking_map_url text,
  pricing_unit text check (pricing_unit in (
    'Per Person', 'Per Mother/Daughter Pair'
  )),

  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);
create trigger classes_updated_at before update on classes
  for each row execute function set_updated_at();
create index classes_is_active_idx on classes (is_active);
create index classes_type_idx on classes (type);


